package com.example.listviewlesson2;

public class Reindeer {

    int image;
    String name;
    public Reindeer(String name, int image)
    {
        this.image = image;
        this.name = name;
    }
    public String getName()
    {
        return name;
    }
    public int getImage()
    {
        return image;
    }
}
